              <p align="justify"><strong>Баннер</strong> – это, как правило, небольшое изображение, которое размещается на различных интернет-ресурсах. Он необходим для продвижения товаров, услуг или иных рекламных проектов. Залог успешного баннера – высокий CTR (показатель кликабельности), который можно обеспечить, благодаря грамотной работе дизайнеров. Кроме того, баннер может побуждать посетителя совершить какое-либо действие (приобрести товар, зарегистрироваться на сайте, заполнить форму и т.д.). Изображение должно не только привлекать внимание пользователя, но и содержать полезную информацию о рекламируемом товаре или услуге.</p>
<p align="justify"><strong>Существует несколько видов баннеров:</strong></p>
<ol>
<li><strong>Статичные баннеры. </strong>Представляет из себя неподвижную картинку, которая содержит дополнительные элементы.</li>
<li><strong>Анимированные баннеры. </strong>Представляют из себя картинки, которые сменяются через определенный интервал времени. Как правило, имеют большой объем, что негативно сказывается на скорости загрузки страницы.</li>
<li><strong>Flash</strong><strong>-баннеры. </strong>Наиболее оптимальный вид баннеров, которые сочетают в себе небольшой размер и разнообразие эффектов. Разрабатываются с помощью программы Adobe Flash.</li>
</ol>
<p align="justify">Наша веб-студия специализируется на разработке статичных баннеров различных размеров (стандарт - 468x60, 120x60, 100x100, 88x31) и форматов (jpg, gif, png). Обратившись к нам, вы получите информативный и привлекательный баннер обеспечивающий высокий CTR, который, в свою очередь, принесет дополнительную прибыль вашему проекту.</p>
