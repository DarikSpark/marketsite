              <h2>Разработка логотипа</h2>
<p>Логотип – это небольшое изображение, которое позволит сообщить потенциальному клиенту о деятельности компании и идентифицировать ее на рынке услуг. Это первый шаг, который необходим для повышения узнаваемости вашего бизнеса. Грамотно разработанный логотип станет толчком к развитию проекта, а в долгосрочной перспективе, он сможет превратиться в серьезный бренд.</p>
<p>Несмотря на кажущуюся простоту, процесс разработки отнимает много времени и сил. Для создания запоминающегося логотипа необходимо владеть информацией о целевой аудитории, знать ситуацию на рынке, и само-собой, отлично разбираться в прикладном софте. </p>
<p><img title="Этапы создания логотипа" src="../../resources/razrabotka_logotipa_vkusnodorogo.jpg" alt="как мы разрабатываем логотип" width="1170" height="730" /></p>
<h2>Логотипы бывают нескольких видов:</h2>
<p>1) Текстовый. Надпись названия компании, которая выполнена различными шрифтами. Может быть разбавлена небольшими графическими элементами.</p>
<p>2) В виде символа. Картинка, которая символизирует деятельность компании.</p>
<p>3) Комбинация текста и логотипа. Здесь присутствует название компании и картинка, символизирующая ее деятельность. Комбинация предыдущих двух вариантов.</p>
<p>4) В виде эмблемы. Самый сложный вид, который сочетает в себе все предыдущие варианты, оформленные в определенном стиле.</p>
<p><img style="vertical-align: baseline;" title="Разработка и создание логотипа" src="../../resources/news/16/02/20/artsfon.com-76653.jpg" alt="Логотип для сайт" width="1379" height="776" /></p>
<p>Чтобы повысить эффективность логотипа, необходимо сделать его не только уникальным, но и узнаваемым среди клиентов. Мы представим на выбор несколько вариантов, а вы, в свою очередь, сможете выбрать наиболее приглянувшийся. Кроме того, наши специалисты помогут создать логотип в <a href="razrabotka-firmennogo-stilya/default.htm" target="_blank">фирменном стиле</a>, который подчеркнет особенности вашей компании, а также сможет привлечь внимание широкой аудитории. </p>
