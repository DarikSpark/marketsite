	  <h2> Какие преимущества лендинга перед обычным сайтом?</h2>
<ul>
<li><strong>Рекламное предложение</strong> на странице лендинга всегда конкретно (один товар или услуга), что сокращает время на принятие решения посетителя</li>
<li>Навигация лендинга максимально<strong> проста и удобна</strong>, т.е. не нужно искать нужный товар в недрах сайта</li>
<li>Создание лендинг-пейдж проще с технической точки зрения, поэтому быстрее по времени и <strong>дешевле по цене</strong></li>
<li>Такая страница <strong>быстро загружается</strong>, т.к. отсутствует большое количество графических и других элементов, замедляющих этот процесс</li>
<li> <strong>Компактность</strong> - несколько лендинг-страниц могут быть размещены на одном домене</li>
<li><strong>Удобство</strong> в отслеживании статистики</li>
<li>Создается клиентская база из покупателей благодаря <strong>формам обратной связи</strong>, при работе с которой можно достичь увеличения продаж до 30%. </li>
<li><strong>Конверсия</strong> одностраничных сайтов в разы <strong>выше</strong>, чем у обычных сайтов с глубокой структурой. Благодаря этому каждый 10-20 посетитель лендинга превращается в вашего клиента.</li>
</ul>
<p><br />Главный <strong>недостаток</strong> лендинг-пейдж - это ограниченность представленной информации, поэтому некоторые клиенты могут быстро его покинуть. Для избежания такого исхода обычно размещаются <strong>положительные отзывы</strong>, <strong>рекомендации</strong>, <strong>награды</strong> и <strong>сертификаты</strong> компании.</p>
<p>При создании продающей страницы мы используем <strong>только</strong> <strong>проверенные и реально работающие техники</strong>!</p>
<h2>Как происходит создание лендинга в нашей студии:</h2>
<p>Шаг №<strong>1</strong>: <strong>Сбор различной информации</strong> о состоянии рынка, конкурентах и предполагаемой аудитории.</p>
<ul>
<li>Определение <strong>целевой аудитории</strong> и ее разделение на группы</li>
<li>Определение основных потребностей аудитории</li>
<li>Определение <strong>сильных сторон</strong> товара или услуги</li>
<li><strong>Анализ</strong> и выявление <strong>конкурентов</strong></li>
<li>Создание предложения, от которого <strong>клиент не сможет отказаться</strong></li>
<li><strong>Прогнозируем</strong> всевозможные <strong>сомнения</strong>, которые могут возникнуть у клиента и работаем над их устранением</li>
</ul>
<p>Шаг №<strong>2</strong>: <strong>Создаем продающую структуру одностраничника</strong> на основании полученных данных.</p>
<p>Шаг №<strong>3</strong>: <strong>Разрабатываем уникальный и продающий дизайн</strong> на основании утвержденной структуры страницы.</p>
<p>Шаг №<strong>4</strong>: <strong>Работа над технической стороной сайта</strong> (программирование, верстка, регистрация домена, размещение на хостинге, настройка почты).</p>
<ul>
<li><a href="adaptivnaya-verstka.html">Адаптивная верстка</a> или создание <a href="../dopolnitelnyie-uslugi/razrabotka-mobilnoj-versii-sajta.html">мобильной версии</a> для вашего одностраничника (возможно использование параллакс эффектов).</li>
<li>Запуск <strong>веб-аналитики</strong> и специальных модулей, которые способны повысить конверсию (например – jivosite и callbackhunter).</li>
<li>Создание нескольких лендингов заточенных под определенное ключевое слово (<strong>мультилендинг</strong>).</li>
<li>Благодаря utm-меткам вы будете видеть <strong>информацию о клиенте</strong> (откуда зашел и по какому запросу).</li>
<li>Мы <strong>информируем</strong> обо всех изменениях на каждом этапе работы.</li>
</ul>
<p>Шаг №<strong>5</strong>: <strong>Привлечение целевых посетителей на лендинг</strong>. Оптимизация сайта под необходимые запросы.</p>
<ul>
<li>Работа с системами <a href="../prodvizhenie-sajtov/kontekstnaya-reklama.html">контекстной рекламы</a>: Яндекс.Директ, Google AdWords.</li>
<li><a href="../prodvizhenie-sajtov/targetingovaya-reklama-v-vk.com,-facebook.html">Таргетированная реклама</a> в различных социальных сетях (Вконтакте, Одноклассники).</li>
<li><a href="../prodvizhenie-sajtov/nastrojka-tizerov-v-marketgid.html">Тизерная реклама</a> в MarketGid и других тизерных сетях.</li>
<li>Ретаргетинг/Ремаркетинг.</li>
<li><a href="../prodvizhenie-sajtov/vnutrennyaya-optimizacziya-sajta.html">Оптимизация</a> под поисковые запросы и <a href="../dizajn-i-grafika/sozdanie-statichnyix-bannerov.html">баннерная реклама</a>.</li>
</ul>
<p>Шаг №<strong>6</strong>: <strong>Мониторинг веб-аналитики</strong>, проведение А-В тестов.</p>
<ul>
<li><strong>Мониторинг</strong> данных Google Analytics и Яндекс.Метрики.</li>
<li><strong>Определение</strong> максимальной <strong>конверсии</strong> посадочной страницы на основании А-В тестирования.</li>
<li>Удаление <strong>неэффективных</strong> рекламных площадок.</li>
</ul>
