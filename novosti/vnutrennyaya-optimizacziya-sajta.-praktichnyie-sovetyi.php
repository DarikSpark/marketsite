              <p><span style="line-height:../1.5em;">Если говорить о <a href="../uslugi/prodvizhenie-sajtov/vnutrennyaya-optimizacziya-sajta.html">внутренней оптимизации</a>, то скорей всего вы уже имеете понятие, что такое мета-теги и ключевые слова. На самом деле под внутренней оптимизацией понимается нечто большее. У нас есть для вас ценный список советов по внутреннему <a href="../uslugi/prodvizhenie-sajtov/default.htm">SEO</a>, который пригодится в дальнейшем. Вы сможете его просматривать и использовать эти рекомендации при продвижении вашего сайта. Он поможет сделать всё, чтобы увеличить трафик и посещаемость. В общем, давайте начнем.</span></p>
<h3>Пользуйтесь понятными URL</h3>
<p>Самое большое значение имеют несколько слов, которые стоят в начале URL</p>
<p>Поэтому, советуем делать адреса страниц короткими и понятными. Будет лучше, если в них вы включите ключевые слова.</p>
<p>Вывод:</p>
<p>• Откажитесь от <em>некрасивых</em> адресов: redline.by/?p=562</p>
<p>• Откажитесь от <em>длинных</em> адресов: /novosti/kak-sdelat-tekst-na-landing-page-privlekatelnyim/kak-povyisit-ctr-(pokazatel-klikabelnosti)-v-vkontakte/stroenie-sajta-i-otdelnyix-stranicz-na-osnove-yandex.wordstat.html</p>
<h3>Начинайте заголовок с ключевой фразы</h3>
<p>Тег Title является <strong>самым</strong> важным элементом для SEO.</p>
<p>Лучше, если keyword будет стоять как можно ближе к началу. Таким образом, поисковая система обратить на него больше внимания. Это не значит, что нужно всё время начинать Title с ключевого слова. Делайте это лишь в тех запросах, которые наиболее важны с вашей точки зрения.</p>
<p><img title="Заголовок Title" src="../resources/news/16/7/SEO.PNG" alt="" width="586" height="91" /></p>
<h3>Используйте в заголовке слова, которые идут с ключевыми</h3>
<p>Перед написанием заголовка подумайте, с какими словами вместе люди ищут вашу информацию в поиске? Возьмите несколько слов, чтобы конкретизировать запрос. Например, «<em>2015</em>», «<em>отзывы</em>», или «<em>обзор</em>». Эта манипуляция направлено на то, чтобы собрать на сайт целевой трафик только тех людей, которым интересна информация именно у вас. А сам сайт будет выстраиваться по низкочастотным запросам.</p>
<h3>Поставьте заголовок статьи в тег H1</h3>
<p>H1 – это тег в заголовке. Многие системы управления содержанием сайта сами ставят заголовок поста в H1. Но не во всем системах есть эта функция. Не волнуйтесь. Всего лишь нужно просмотреть исходный код страницы и удостовериться, что название стоит в теге H1.</p>
<h3>Добавляйте на сайт мультимедийное содержание</h3>
<p>Одного текста на странице будет недостаточно. К нему лучше приложить различные картинки, видео, рекламу, диаграммы. Всё это обратит на себя внимание пользователя и заставит его задержаться на сайте. Это в свою очередь снизит количество отказов и увеличит время пребывания на сайте. А ведь это одни из самых главных факторов, которые поисковая система принимает во внимание.</p>
<h3>Поставьте подзаголовки в тег H2</h3>
<p>Неплохо использовать хотя бы одно ключевое слово в подзаголовке. Сделайте это и поставьте его в тег H2. Этот маленький трюк позволяет немного улучшить внутреннюю оптимизацию.</p>
<h3>Используйте ключевые слова в первых сто словах статьи</h3>
<p>В большинстве случаев это и так получается делать неосознанно. Но иногда создатели статей отвлекаются на слишком большое по объему введение. В итоге ключевая фраза оказывается чуть ли не в самом конце поста. Поэтому советуем вставить ключевой запрос в самое начало. Примерно в первые сто слов. Это лишь поможет поисковой системе понять, в чем суть вашего контента на сайте.</p>
<p><img title="Ключевые слова" src="../resources/news/16/7/Key.PNG" alt="" width="734" height="180" /></p>
<h3>Используйте дизайн под мобильные устройства</h3>
<p>Год назад были введены санкции против сайтов, которые не были приспособлены под мобильные устройства. Надо предполагать, что в дальнейшем это будет иметь еще большее значение. Ведь с появлением смартфонов и планшетов люди стали больше времени проводить в интернете с мобильных устройств. Поэтому задумайтесь над внедрением адаптивного дизайна, чтобы просмотр с телефонов был более удобным.</p>
<h3>Добавляйте исходящие ссылки</h3>
<p>Это достаточно просто. Просто используйте ссылки нас страницы, связанные по тематике с вашей. Это даст поисковой системе возможность понять о чем ваша страница. А также она будет выглядеть как источник достоверной информации. Ни в коем случае нельзя допускать отсутствия исходящих ссылок! Их может быть немного, от двух до пяти на 1000 слов, но они должны быть. И не забывайте, что сайты, на которые вы делаете ссылку, оказывают влияние на ваш сайт. Так что проверьте, чтобы сайт ссылался на компетентные ресурсы.</p>
<h3>Добавляйте внутреннюю перелинковку</h3>
<p>Огромное значение имеет наличие внутренних ссылок. Посмотрите на Википедию, для примера. Каждая статья пестрит большим количеством ссылок на внутренние страницы.</p>
<p><img title="Внутренняя перелинковка" src="../resources/news/16/7/perelink.PNG" alt="" width="763" height="253" /></p>
<p>Используйте несколько перелинковок в каждом посте. Оптимальным количеством будет 2-5 для одной статьи.</p>
<h3>Попробуйте увеличить скорость загрузки сайта</h3>
<p>Несложно догадаться, что <a href="../uslugi/prodvizhenie-sajtov/uvelichenie-skorosti-zagruzki-sajta.html">продолжительность загрузки сайта</a> – это довольно важный фактор. Попробуйте несколько решений этой проблемы. Например, вы можете сжать изображение или сменить хостинг на более шустрый. Для начала проверьте: сколько по времени загружается ваш сайт? Если вам приходится ждать дольше 4 секунд – это отрицательный показатель. Исследования показали, что люди больше не посещают сайт, который грузится более 4 секунд. Проверьте вашу скорость с помощью GTMetrix. Совет: вложитесь в премиум хостинг. Плата за него вполне приемлемая.</p>
<h3>Внедрите связанные ключевые слова</h3>
<p>LSI keywords – это слова-синонимы, которыми пользуется поисковая система, оценивая релевантность вашего сайта. Их нужно добавлять в каждый пост. Если у вас получается большой по объему контент, эти слова и так будут присутствовать естественным образом. Но если вы не совсем уверены в том, что их достаточно, можно проверить себя следующим образом. Введите ваш запрос в Google и прокрутите вниз. В конце страницы будет фраза «<em>Вместе с … часто ищут</em>».</p>
<p><img title="Связанные ключевые слова" src="../resources/news/16/7/skw.PNG" alt="" width="526" height="252" /></p>
<p>Используйте несколько фраз отсюда в своем посте.</p>
<h3>Оптимизируйте изображения</h3>
<p>Проверьте названия изображений. Хотя бы несколько из них должны содержать ключевое слово и присутствовать в ALT тексте. Этот пункт полезен еще тем, что дает возможность поисковым системам понять, о чем сайт. Это помогает в ранжировании. Вот например, Google видит картинки с Atl текстом «крупный шрифт» и «мелкий шрифт», и понимает, что страница про шрифты.</p>
<h3>Используйте кнопки репоста в социальные сети</h3>
<p>Данный метод не оказывает должного влияния на позиции сайта. Тем не менее, позволяет увеличить количество просмотров страниц. Так что попробуйте разместить кнопки социальных сетей на видном месте страницы. Это позволит достаточно увеличить шаринг. Добавляйте кнопки на Вконтакте, Одноклассники или Facebook.</p>
<p><img title="Кнопки репоста" src="../resources/news/16/7/Repbutton.PNG" alt="" width="219" height="48" /></p>
<h3>Публикуйте большие по объему статьи</h3>
<p>Объемный контент на сайте ранжируется гораздо лучше на первой странице выдачи поисковой системы. В каждой статье используйте примерно 2000 слов.</p>
<h3>Увеличьте время, проводимое на странице</h3>
<p>Как поисковая система определяет качество страницы? По времени, которое человек на ней проводит. Если зайдя на страницу, человек сразу же возвращается к результатам поиска, это плохой показатель. Google использует <em>dwell time</em>. С помощью этого он определяет качество контента на сайте. Вы можете увеличить время, которое пользователи проводят на сайте. Для этого вам нужно создать интересный контент, который увлечет пользователей и они останутся.</p>
<h3>Качественное содержание сайта</h3>
<p>Об этом уже говорилось не один раз. Но мы повторим: сама поисковая система не определяет качество контента. Но у нее есть множество методов, с помощью которых она может понять ценность содержания сайта. Например:</p>
<p>• Закладки в браузере</p>
<p>• Dwell time, о котором писалось выше</p>
<p>• Брендовые запросы</p>
<p>Итог: сразу публикуйте контент хорошего качества.</p>
<h3>Создайте возможность комментировать блог</h3>
<p>Важно даже не само количество комментариев или их качество. Главное в том, что аудитория должна постоянно увеличиваться и обновляться, активно общаться между собой. Не стоит упускать возможность самим общаться с пользователями, отвечая на их вопросы. Это отлично помогает в ранжировании.</p>
<p><img title="Комментарии блога" src="../resources/news/16/7/Comments.PNG" alt="" width="765" height="671" /></p>
<h3>Увеличьте CTR</h3>
<p>Чаще всего поисковая система использует показатель кликабельности сниппета в выдаче. Но если это не совсем так, все равно будет лучше оптимизировать CTR. Чем больше кликов, тем больше трафика вашему сайту.</p>
<h3>Улучшите иерархию сайта</h3>
<p>В большинстве случаев основная страница сайта – самая авторитетная. А это значит, что чем ближе расположена страница к основной – тем она будет выше в топе поиска. Что нужно сделать? Уменьшить количество кликов от главной страницы до той, которую вы продвигаете. Кликов должно быть не больше трех, а то и меньше.</p>
<p>А теперь проверьте внутреннюю оптимизацию своего сайта. Надеемся, что наши советы вам помогли!</p>
