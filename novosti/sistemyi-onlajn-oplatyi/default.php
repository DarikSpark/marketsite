              <p class="justifyleft"><span style="line-height:../../1.5em;">Что первое приходит на ум, когда мы слышим слово «интернет-магазин»? Скорей всего, большинство из нас представляют список товаров с ценами. Также на сайте будет присутствовать номер телефона, по которому можно позвонить и заказать товар. С развитием интернет-технологий, большое значение приобрели <strong>электронные платежи</strong>. Поэтому, неплохо добавить возможность оплаты электронными деньгами на вашем сайте. Для товаров с высокой ценой это может не совсем применимо, но возможность выбора способа оплаты вызовет у клиента <strong>доверие</strong> и <strong>интерес</strong> к вашему магазину.</span></p>
<h2 style="text-align: center;"><span style="line-height:../../1.5em;">Виды систем оплат</span></h2>
<p class="justifyleft">Самый распространенный способ оплаты за товар - это заплатить деньги лично курьеру, после доставки им товара вам на дом. Но в настоящее время появился еще один не менее популярный способ – <strong>оплата банковскими картами</strong>. Скорей всего, сейчас они уже есть почти у каждого человека. Практически во всех организациях зарплата приходит на карточку, а в учебных заведениях на карту начисляют стипендию. Это быстро и удобно. Поэтому, внедрить данный способ оплаты на ваш сайт будет <strong>выгодно</strong>. Потребуется лишь несколько манипуляций: ввести нужные данные, указанные на карте, и нажать «Оплатить». Всё, оплата будет произведена.</p>
<p class="justifyleft">Есть еще один способ. Это оплата с помощью <strong>электронных денег.</strong> Такую возможность предоставляют платежные системы <em>«EasyPay»</em> или <strong>«WebMoney»</strong>. Но этот способ занимает больше времени и усилий. Перед тем, как начать пользоваться данными системами, человек сначала должен создать свой электронный кошелек. А для полноценного пользования кошельком и повышения его статуса необходимо еще и подтвердить свои данные, съездив в банк с паспортом.</p>
<p class="justifyleft">Перед тем, как представить вам основные системы оплаты, которыми пользуются жители Беларуси, выясним основные их<strong> плюсы</strong>, и что вам даст подключение их к вашему магазину в интернете.</p>
<h2 style="text-align: center;">Плюсы электронных систем оплаты</h2>
<p class="justifyleft">1. <strong> Практичность.</strong> Некоторые люди принципиально ищут <a href="../../uslugi/razrabotka-sajtov/internet-magazin.html">интернет-магазин</a>, который имеет такую возможность, как принимать системы оплаты. Для такого человека стоимость товара уже не будет играть большого значения.</p>
<p class="justifyleft">2.  <strong>Доверие.</strong> Людям нравится, когда у магазина удобный и достойный сервис. Таким образом, ощущается забота о клиентах и заинтересованность в их наличии.</p>
<p class="justifyleft">3.  <strong>Предусмотрительность.</strong> Это удобно не только клиентам, но и работникам интернет-магазина. Например, курьеру. Он обеспечивает безопасность и сохранность денег, не перевозя их с собой.</p>
