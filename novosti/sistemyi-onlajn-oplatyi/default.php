<p class="justifyleft">Из всех представленных, система <em>«Расчет»</em> является самой привлекательной. Комиссию берет небольшую, а охват – довольно широкий. Ведь с помощью <em>ЕРИП</em> можно оплатить всё что угодно и чем угодно. Правда, у этой системы есть небольшой недостаток: человек сам должен найти нужный ему интернет-магазин в списке, который предоставляет <em>ЕРИП</em>. А вот<em> ASSIST</em> в этом плане более удобен: клиент имеет возможность непосредственно сразу перейти к оплате после вашего магазина.</p>
<h2 style="text-align: center;">Добавление функции осуществления платежей на сайте</h2>
<p class="justifyleft">Для начала, вам предстоит решить: какие конкретно системы оплаты у вас есть желание добавить на сайт? После принятого решения, вам предстоит обратиться в фирму и заключить договор. После подписания договора, вы получите доступ к вашему личному кабинету и описание действий, необходимых для введения системы оплаты на сайт. Техническую часть будет лучше доверить программистам, которые создавали ваш <a href="../../uslugi/razrabotka-sajtov/default.htm">сайт</a>. Мы предоставляем <a href="../../uslugi/dopolnitelnyie-uslugi/podklyuchenie-platezhnoj-sistemyi-dlya-sajta.html">услугу</a> внедрения платежных систем к сайтам интернет-магазинов на 1С-Битрикс.</p>
<p class="justifyleft"><strong>При заключении договора, обратите внимание на следующее:</strong></p>
<p class="justifyleft">1.  <strong>Величина комиссии.</strong> Оператор берет комиссию за прием оплаты. Решите сразу, кто будет выплачивать данную комиссию. Это может быть клиент или вы сами. Лучший вариант: оплачивайте сами, не возлагая дополнительную сумму к товару на покупателя.</p>
<p class="justifyleft">2. <strong>Скорость перевода средств.</strong> Как будет происходить перевод денег за товар? Автоматически или с открытием счета в банке.</p>
