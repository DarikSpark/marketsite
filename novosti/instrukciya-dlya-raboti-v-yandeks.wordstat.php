              <p align="justify"><strong>Яндекс.Wordstat</strong> – это сервис, без которого невозможно <a href="../uslugi/prodvizhenie-sajtov/default.htm">продвижение</a> ни одного серьезного проекта. Основным назначением вордстата является <strong>сбор</strong> поисковых запросов, которые необходимы для составления семантического ядра сайта и написания SEO-оптимизированного контента. В данной статье мы подробно распишем функционал сервиса и узнаем, как можно применить полученную информацию на практике.</p>
<p align="justify">Для многих Яндекс.Wordstat это всего лишь инструмент, который позволяет составить <a href="../uslugi/prodvizhenie-sajtov/sbor-semanticheskogo-yadra-sajta.html">семантическое ядро</a> и <a href="napolnenie-sajta-kontentom.html">наполнить сайт </a>оптимизированным контентом, но это далеко не так. Вордстат имеет <strong>множество функций</strong>, о которых мы поговорим далее.</p>
<h2><strong>Как работать в Яндекс.</strong><strong>Wordstat</strong></h2>
<p align="justify">Благодаря минималистичному и интуитивно понятному интерфейсу, работа с Яндекс.Wordstat не должна доставить вам никаких проблем. Кроме того, сервис хорошо <strong>адаптирован</strong> для мобильных устройств, что является большим плюсом. Для того, чтобы начать работу с вордстат, вам необходимо сделать следующее:</p>
<ol>
<li>Авторизироваться в почтовом ящике Яндекса.</li>
<li>Перейти на страницу сервиса.</li>
<li>Набрать в строке необходимое вам слово / словосочетание / предложение и кликнуть на кнопку «Подобрать».</li>
</ol>
<p align="justify">Если у вас нету зарегистрированной почты на Яндексе, то это совсем не повод убегать в поисках других сервисов. Регистрация происходит в несколько кликов и не займет больше 5 минут. После того, как аккаунт зарегистрирован, заходим непосредственно на сам сайт - <!-- noindex--><a href="../../https@wordstat.yandex.ru/default.htm" rel="nofollow">https://wordstat.yandex.ru/</a><!--/noindex-->.</p>
<p align="center"> <img title="Так выглядит окно Яндекс Wordstat" src="../resources/image002.jpg" alt="Так выглядит окно Яндекс Wordstat" width="624" height="510" /></p>
<p align="center"><em>Вот так выглядит главная страница Яндекс.</em><em>Wordstat</em><em>.</em></p>
<p>Теперь вам нужно ввести запрос и кликнуть на кнопку «Подобрать». Готово, через пару секунд вы увидите результат (а возможно и не увидите, данный сервис не славится особой стабильностью). Кроме того, вордстат умеет работать с ключевыми словами в <strong>разных числах</strong> и <strong>падежах</strong>. Убедиться в этом, мы можем вбив в строку поиска словосочетание «популярные поисковые запросы».</p>
<p style="text-align: center;"> <img title="В словосочетании Яндекс Wordstat могут встречаться дополнительные слова " src="../resources/image004.jpg" alt="В словосочетании Яндекс Wordstat могут встречаться дополнительные слова " width="624" height="993" /></p>
<p align="center"><em>Стоит отметить, что в словосочетании могут встречаться дополнительные слова и различные окончания.</em></p>
<p>Как видно на скриншоте, сразу же после строки ввода располагается переключатель, который позволяет отсортировать запросы <strong>по типу устройств</strong>: все (сюда включены все запросы), только мобильные (запросы, которые были сделаны с планшетов, телефонов и т.д.).</p>
<h3 align="center"><strong>Символы уточняющие ваш запрос  </strong></h3>
<p>Как и в поисковой системе, в Яндекс.Wordstat можно применять <strong>дополнительные символы</strong>, которые позволят конкретизировать ваш запрос. В таблице ниже приведены самые распространенные из них.</p>
