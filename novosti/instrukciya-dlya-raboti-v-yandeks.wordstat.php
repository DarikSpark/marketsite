<p align="center"><strong>Все еще не понятно? Хорошо, объясним более подробно.</strong></p>
<p>1) В первом случае, знак минус перед запросом позволяет <strong>убрать</strong> из результатов все словосочетания, где оно упоминается. Для большей наглядности приведем пример. При вводе запроса «майки <strong>– белые</strong>» мы отсечем такие запросы как «купить белые майки», «белые майки фото» и т.д.</p>
<p>2) Во втором случае, знак плюса перед запросом позволяет в принудительном порядке <strong>вывести</strong> необходимые словосочетания, в которых встречается данное слово. Стоит отметить, что чаще всего сервис не отображает большинство служебных частей речи. То есть, если вы набираете «продвижение сайтов в Витебске», то в результатах будут встречаться такие запросы как «продвижение сайтов Витебск», «заказать продвижение сайтов Витебск» и т.д. А набирая запрос «продвижение сайтов <strong>+в</strong> Витебске», в результатах отобразятся лишь те словосочетания, которые имеют в своем составе предлог «в».</p>
<p>3) В третьем случае, отображаются только слова <strong>«обрамленные в кавычки»</strong>. Как уже упоминалось ранее, их последовательность и окончания могут быть различны.</p>
<p>4)  А в последнем случае, мы получим <strong>точное</strong> вхождение запроса (без изменения числа и падежа).</p>
<h3 style="text-align: center;"><strong>Региональные запросы</strong></h3>
<p>Яндекс.Wodrstat умеет сортировать запросы в зависимости от <strong>географического расположения</strong>. Для этого всего лишь нужно найти кнопку «Все регионы» и выбрать необходимые опции. Кроме того, в правой колонке вы можете осуществить быстрый выбор наиболее популярных географических вариантов.</p>
<h3 align="center"><strong>Ищем низкочастотные запросы</strong></h3>
<p>Сервис упорядочивает запросы от высокочастотным к низкочастотным. То есть в первую очередь отображаются самые популярные запросы, а в конце списка идут наименее популярные. Одним кликом невозможно перейти в самый конец списка, для этого придется листать вперед по одной странице. Стоит отметить, что при быстром пролистывании может появится капча.</p>
<p style="text-align: center;"><img title="Так выглядит пагинация в сервисе Яндекс.Wordstat" src="../resources/image006.jpg" alt="Так выглядит пагинация в сервисе Яндекс.Wordstat" width="624" height="68" /> </p>
<p align="center"><em>Так выглядит пагинация в сервисе Яндекс.</em><em>Wordstat</em></p>
<p>К сожалению, в сервисе нету встроенных инструментов, которые позволяют сохранять список запросов в файл Excel или Word. Но вы можете выделить результаты, нажать Ctrl + C и вставить их в Excel в виде текста без форматирования.</p>
<h3 align="center"><strong>Что делать когда запросов слишком много?</strong></h3>
<p>Система Яндекс.Wordstat имеет небольшой минус. Сервис формирует <strong>не более</strong> 40 страниц запросов. Особенно это мешает, когда вы вбиваете очень популярный запрос, например «скачать фильм».</p>
<p style="text-align: center;"> <img title="Практически 5 000 000 запросов в выдаче Яндекс Wordstat" src="../resources/image008.jpg" alt="Практически 5 000 000 запросов в выдаче Яндекс Wordstat" width="624" height="998" /></p>
<p align="center"><em>Практически 5 000 000 запросов. Вряд ли мы увидим конец выдачи на 40-ой странице.</em></p>
<p>Но выход есть. И реализован он, естественно, с помощью сторонних разработчиков. Существует множество инструментов, которые позволяют увидеть всю выдачу. Данные инструменты именуются <strong>парсерами</strong>. Самый известный и функциональный - Key Collector, однако за него придется заплатить. К счастью, разработчик предусмотрел бесплатную версию с ограниченным функционалом, которой вы можете воспользоваться в любой момент.</p>
<h3 align="center"><strong>Колонка «похожие запросы»</strong></h3>
<p>Наверняка, многие из вас заметили справа от результатов еще одну колонку. Ее содержание формируется исходя предыдущих запросов пользователя. Проще говоря, из правой колонки мы сможем узнать каким образом люди пытаются решить свои проблемы.</p>
<p>Кстати, кликнув на любое словосочетание из «похожих запросов», оно переместиться в поле для ввода и вы сразу же получите результаты по выбранному запросу. Иногда в этой колонке встречаются откровенно мусорные запросы, но встречаются и важные запросы, которые позволяют расширить семантическое ядро.</p>
<h3 align="center"><strong>Другие возможности Яндекс.</strong><strong>Wordstat</strong></h3>
<p>Стоит отметить, что сервис предусматривает возможность фильтровать выдачу по <strong>различным параметрам</strong>. Фильтр «история запросов» позволяет определить их сезонность. То есть узнать какие запросы популярны в определенное время года. Вордстат отобразит всю информацию с помощью наглядных графиков.</p>
<p><strong>Подводим итоги:</strong></p>
<ul>
<li>Сервис полностью бесплатен.</li>
<li>Для его использования необходима регистрация.</li>
<li>Выдача запросов зависит от многих параметров (например, сезонность).</li>
<li>Есть возможность отфильтровать выдачу.</li>
<li>С его помощью можно составлять семантическое ядро и писать оптимизированные статьи.</li>
</ul>
