              <p class="justifyfull">Продвижение с помощью SSM (Social Media Marketing) является <strong>действенным</strong> средством к достижению раскрутки вашего сайта. Данный процесс на сегодняшний день достаточно популярен и востребован. А всё потому, что большая часть ваших потенциальных клиентов зарегистрирована и обитает в таких <a href="../uslugi/prodvizhenie-sajtov/prodvizhenie-grupp-i-publichnyix-stranicz-v-soczialnyix-setyax.html" target="_blank">социальных сетях</a>, как ВКонтакте, Одноклассники, Facebook, Instagram и др. Этот способ продвижения можно еще назвать «внешняя оптимизация портала», так как для продвижения мы работаем с внешними средствами, избегая работ на самом сайте. Главным принципом SMM является <strong>привлечение</strong> людей из соцсетей. Чтобы этого добиться, нам придется поработать с самой соцсетью. Данный способ оптимизации сайта вполне <strong>доступен</strong> по цене даже начинающим!</p>
<p class="justifyfull">Вот несколько достоинств продвижения с помощью SMM</p>
<p class="justifyfull"><strong>Достоинства SMM-продвижения</strong></p>
<ul class="justifyfull">
<li>Самое главное – продвижение вашего сайта, продукции, интернет-магазина и т.д.</li>
<li>Рост количества гостей на сайте</li>
<li>Раскрутка вашего бренда</li>
<li>Создание ненавязчивой рекламы вашего сайта</li>
<li>Увеличение социального трафика</li>
<li>Лавинный эффект</li>
</ul>
<h2 class="justifyfull">Принцип работы с SMM</h2>
<p class="justifyfull">Итак, теперь подробнее о том, как работает SMM.</p>
<p class="justifyfull"><strong>Создание начальной платформы</strong></p>
<p class="justifyfull">- Начать можно с создания группы/паблика/информационной страницы в соцсети. Данный способ подойдет для таких сетей, как Вконтакте и Facebook. Если же вы хотите продвигать свой товар через Tweeter, тогда нужно будет создать микроблог и вести его.</p>
<p class="justifyfull">- После создания группы/блога займитесь его <strong>наполнением</strong>. Придумайте привлекающее название, красиво оформите аватар группы и меню (если таковое имеется), залейте фотографии с вашего сайта. Не забудьте создать раздел Вопросы и Ответы, где пользователи смогут задавать интересующие их вопросы и своевременно получать на них от вас ответы. Добавьте на страницу <strong>ссылки</strong> на ваши внешние сайты и контакты.</p>
<p class="justifyfull"><strong>Тактика привлечения</strong></p>
<p class="justifyfull">После того, как вы поймете, что ваш паблик готов, начинайте привлечение в него пользователей сети. С разрешения администрации популярных групп, можете <strong>рекламировать</strong> свою группу через них или публиковать информацию о группе на личных страницах, в играх. Данный способ является абсолютно <strong>бесплатным</strong>, но потребует времени, терпения и коммуникативных навыков.</p>
<p class="justifyfull"><img title="Привлечение посетителей" src="../resources/news/16/7/miu.jpg" alt="" width="1000" height="600" /></p>
<p class="justifyfull"><span style="line-height:../1.5em;">Также вы можете воспользоваться услугой </span><a style="line-height: 1.5em;" href="../uslugi/prodvizhenie-sajtov/kontekstnaya-reklama.html" target="_blank">контекстной рекламы</a><span style="line-height:../1.5em;"> в социальной сети. Такая реклама настраивается с помощью </span><strong style="line-height: 1.5em;">таргетинга</strong><span style="line-height:../1.5em;"> и показывается только определенной целевой аудитории. Эффект от таргетинговой рекламы не заставит себя долго ждать. Данный способ идеально подойдет обладателям </span><a style="line-height: 1.5em;" href="../uslugi/razrabotka-sajtov/internet-magazin.html" target="_blank">интернет-магазинов</a><span style="line-height:../1.5em;">, которым важно прорекламировать и продать свой товар.</span></p>
<p class="justifyfull"><strong>Создание качественного контента</strong></p>
<p class="justifyfull">После всего проделанного, не забывайте о своем паблике ни на сутки. Группа должна быть <strong>живой</strong>! Что это значит? Выкладывайте по несколько интересных постов за день, стимулируйте посетителей к просмотру и общению, отвечайте на их комментарии и вопросы.</p>
<ul class="justifyfull">
<li>Иногда можно повторно публиковать важные заметки</li>
<li>Уделите внимание визуальному оформлению постов. Используйте <strong>привлекающий</strong> текст и картинки. Также сразу определитесь с тоном общения: будет ли он официальным или же неформальным.</li>
<li>Отличным вариантом будет<strong> стимуляция</strong> участников группы самим создавать контент. Предлагайте им опросы, различные розыгрыши и обязательно интересуйтесь отзывами.</li>
<li>Учитывайте<strong> время</strong> публикации материала: новый пост в новостях должно просмотреть как можно больше участников. Поэтому лучше, если это будет утро, время обеда или вечер перед сном.</li>
<li>Сделайте ваш контент <strong>видимым</strong> для поисковых систем. Не забывайте употреблять ключевые слова и указывать на сайте метаданные и хэштеги.</li>
</ul>
<p class="justifyfull"><strong>Достижение конверсии</strong></p>
<p class="justifyfull">Необходимо добиться <strong>конверсии</strong> участников паблика в потенциальных клиентов. Для этого не забывайте оставлять свои контактные данные, при этом поощряйте оставление контактных данных самими пользователями. Хорошим решением будет направлять пользователей соцсетей на <a href="../uslugi/razrabotka-sajtov/lending.html" target="_blank">посадочную страницу</a> вашего сайта.</p>
<p class="justifyfull"><img title="Рост конверсии" src="../resources/news/16/7/ss-101126-black-friday-09.ss_full-960x458.jpg" alt="" width="960" height="458" /></p>
<p class="justifyfull">Помните: чем <strong>больше</strong> посетителей привлечете вы через социальные сети – тем <strong>выше</strong> будет трафик на ваш сайт в интернете. Причем трафик будет<strong> качественным</strong>, за счет только целевой аудитории. А это в свою очередь положительно оценят поисковые системы. Как следствие - позиции вашего сайта в поиске будут расти.</p>
<p class="justifyfull"><strong>Возможные трудности</strong></p>
<p class="justifyfull">Если вы не обделены временем, то такой способ продвижения вам подойдет. SMM-продвижение требует больших временных затрат. Работы будет очень много и делать ее придется <strong>постоянно</strong>.</p>
<p class="justifyfull">Группа, которую вы создали в помощь к продвижению вашего сайта, сама нуждается в продвижении. Не забывайте, это тоже дополнительная трата времени и сил. Скорей всего, придется найти человека, который будет своевременно следить за ее <strong>обновлением</strong> и раскруткой.</p>
<p class="justifyfull"><strong>Оценка результата</strong></p>
