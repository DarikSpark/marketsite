              <p align="justify">Контекстная реклама – наиболее эффективный способ привлечения пользователей на веб-ресурс. Во-первых, она смотрится естественно и не раздражает посетителя. Кроме того, она демонстрируется только тем пользователям, которые действительно заинтересованы Вашим товаром или услугой. Именно эти условия обеспечивают ее высокий CTR (иными словами – кликабельность).</p>
<p align="justify">В рамках данной статьи мы расскажем о том, что такое эффективная контекстная реклама, а также поговорим о том, для каких целей она необходима и как ведутся контекстные рекламные кампании.</p>
<h2 align="center"><strong>Что такое контекстная реклама?</strong></h2>
<p align="justify"><a href="../uslugi/prodvizhenie-sajtov/kontekstnaya-reklama.html">Контекстная реклама</a> – это определенный вид рекламы, который релевантнен той информации, которую пользователь ищет в поисковой системе (чаще всего в Яндекс или Google).</p>
<h2 align="center"><strong>Где располагается реклама Гугл и Яндекс? </strong></h2>
<p align="justify">Приведем простой пример. Вы хотите найти информацию о холодильниках и вбиваете в поисковик запрос «новый холодильник». Вместе с органической выдачей Вам покажутся объявления рекламного характера. Как правило, они располагаются в самом верху выдачи, а также справа и снизу. На данных веб-ресурсах вы найдете информацию об искомом товаре и, при необходимости, сможете купить его.</p>
<p align="justify">Наглядный пример расположения рекламы Гугл и Яндекс:</p>
<p align="justify"> </p>
<p align="center"><img style="border: 1px solid black;" title="Пример эффективной контекстной рекламы в Яндекс" src="../resources/news/16/01/21/Screenshot_26.png" alt="Пример эффективной контекстной рекламы в Яндекс" width="900" height="536" /></p>
<p align="center"><img style="border: 1px solid black;" title="Контекстная реклама Яндекс Директ, блок гарантия" src="../resources/news/16/01/21/Screenshot_27.png" alt="Контекстная реклама Яндекс Директ, блок гарантия" width="900" height="509" /> </p>
<p style="text-align: center;" align="justify"><img style="border: 1px solid black;" title="Пример эффективной контекстной рекламы в Google" src="../resources/news/16/01/21/Screenshot_28.png" alt="Пример эффективной контекстной рекламы в Google" width="900" height="836" /></p>
<p align="justify">Такой способ рекламы товара не раздражает потенциальных клиентов, в отличие от различных баннеров, тизеров и т.д. Контекстная реклама предоставляет действительно полезную и необходимую информацию. Зачастую ее именуют SEM рекламой (от английского – Search Engine Marketing).</p>
<h2 align="center"><strong>Разновидности рекламных сетей</strong></h2>
<blockquote><em>Контекстные объявление могут размещаться не только в выдаче, но и на страницах различных интернет-ресурсов.</em></blockquote>
<p align="justify">Большинство сайтов в интернете не занимаются продажей каких-либо товаров или услуг. Они носят познавательный характер. Данные сайты собирают всю полезную информацию об определенном товаре в одном месте. Если сайт действительно качественный и приносит много посетителей, то на его страницах устанавливается специальный код рекламной сети. Благодаря этому, владелец сайта может зарабатывать за клики на рекламные объявления.</p>
<p align="justify">В данном случае поисковая система в автоматическом режиме определяет содержимое каждой страницы сайта и подбирает соответствующую рекламу. В результате объявления о продаже велосипедов отобразятся на странице с информацией о велосипедах, а объявления о продаже пылесосов, соответственно, на странице с информацией о бытовой технике и т.д.</p>
<p align="justify">Кроме того, для уточнения объявления используются ключевые слова. Например, «<a href="../uslugi/razrabotka-sajtov/korporativnyij-sajt/default.htm">Создание корпоративного сайта в Витебске</a>». Это нужно для того, чтобы системы контекстной рекламы подбирали наиболее релевантные объявления для каждой страницы, что, в свою очередь, позволит увеличить кликабельность рекламы.</p>
<p align="center"><img style="border: 1px solid black;" title="Пример эффективной контекстной рекламы на площадках партнеров Яндекс" src="../resources/news/16/01/21/image007.gif" alt="Пример эффективной контекстной рекламы на площадках партнеров Яндекс" width="523" height="276" /></p>
<h2 align="justify"><strong>Как определить эффективность контекстной рекламы Яндекс и </strong><strong>Google</strong><strong>?</strong></h2>
<p align="justify">Узнав про виды размещения контекстной рекламы появляется резонный вопрос: «А где реклама эффективнее? В поисковой выдаче или на различных сайтах?» Ведь, по сути, предназначение у нее одно – привлечение клиентов на веб-ресурс.</p>
<p align="justify">Множество веб-мастеров, маркетологов и других людей, которые так или иначе связаны с контекстной рекламой, оценивают ее эффективность по показателю кликабельности (CTR) или цене клика (CPC). Однако, более корректно оценивать ее с позиции полезности для пользователя. Как этого можно добиться?</p>
<p align="justify">Посетители, выполнившие необходимое действие на сайте (приобрели товар, заказали услугу, подписались на почтовую рассылку и т.д.), естественно, представляют большую ценность нежели те, кто пришел через контекстную рекламу Яндекса, побродил несколько минут по сайту и покинул его. Для того, чтобы отследить полезность конкретного посетителя нужно использовать счетчики посещений, которые предоставляют подробную статистическую информацию о их действиях.</p>
<p align="justify">В работе мы используем наиболее распространенные из них – Google Analytics и Яндекс.Метрика. Они могут эффективно отслеживать любую рекламную кампанию. Их распространенность обусловлена широким функционалом, который позволят получать самую разнообразную информацию о Ваших посетителях: например, количество пользователей совершивших определенное действие (скачали файл, взаимодействовали с определенным элементом сайта и т.д.).</p>
<p align="justify">Наши специалисты не только помогут узнать количество посетителей на сайте, но и определят их качество и полезность.</p>
<h2 align="justify"><strong>Как происходит настройка контекстной рекламы?</strong></h2>
<p align="justify">Это происходит с помощью специализированных сервисов, которые предоставляют рекламные сети. С помощью них можно создать объявление, подобрать ключевые слова, а также управлять рекламной кампанией в дальнейшем.</p>
<p align="justify">Для настройки контекстной рекламы специалисты студии веб-дизайна RedLine используют наиболее популярные русскоязычные сервисы:</p>
<p align="justify"><strong>1) Яндекс.Директ – </strong>отечественная система, предоставленная компанией Яндекс. Она позволяет размещать объявления в поисковой выдаче, а также на страницах сайтов, где установлены рекламные объявления. Процесс настройки Яндекс.Директ прост и с ним справится даже новичок.</p>
<p align="justify"><strong>2) </strong><strong>Google</strong><strong>AdWords</strong><strong>– </strong>аналогичная система, но предоставленная компанией Google. Функционал во многом схож с Яндекс.Директ. Вторая по популярности система в России.</p>
<h2 align="justify"><strong>Сколько стоит заказать контекстную рекламу?</strong></h2>
<p align="justify">Один из первых вопросов, который мы получаем от своих клиентов. Стоимость контекстной рекламы в веб-студии RedLine начинается от 189 рублей. Однако каких-то конкретных цифр ждать не стоит, так как их просто невозможно высчитать. Основная отличительная черта контекстной рекламы – отсутствие фиксированной цены. Так происходит потому что оплата производится только за клик по объявлению, а не за его показы, что характерно для других видов рекламы (баннерная, тизерная и т.д.).</p>
<p align="justify">Ценообразование клика происходит по принципу аукциона. Каждый рекламодатель определяет свою цену перехода на веб-ресурс. Чем она выше, тем больше вероятность увидеть объявление в наиболее привлекательном месте (например, вверху над поисковой выдачей).</p>
<p align="justify">Прежде, чем заказать контекстную рекламу, стоит помнить, что из-за вышеприведенных факторов цена каждого клика нестабильна и постоянно меняется. Кроме того, на не влияют и другие факторы, такие как ставки конкурентов и применение систем, которые автоматически корректируют цену клика. Данные инструменты позволяют стабилизировать цену клика и производить более эффективное управление, нежели стандартный интерфейс, который предоставляется Яндекс.Директ или Google AdWords.</p>
<p align="justify"><strong>Этапы проведения рекламной кампании </strong></p>
<p align="justify"><strong>1) Составление <a href="gde-brat-klyuchevyie-slova-i-kak-ix-ispolzovat.html">ключевых слов</a>. </strong>Первый и самый важный этап – составление запросов, по которым Ваши объявления будут крутиться в поисковой выдаче или на определенных веб-ресурсах. Это происходит с помощью специальных инструментов, которые предоставляются системами контекстной рекламы. Кроме того, они позволяют подсчитать примерную стоимость клика и стоимость рекламной кампании в целом (как правило, расчет производится на 30 дней вперед). Составление списка ключевых слов специалистами RedLine происходит с использованием дополнительной базы ключевиков, в которой имеется информация о предыдущих кампаниях проводимых нами.</p>
<p align="justify"><strong>2) Создание текста объявлений. </strong>Также немаловажная часть. В достаточно небольшие объявления (50-100 символов) необходимо уместить всю полезную информацию, которая привлечет пользователя и заставит его кликнуть по нему.</p>
<p align="justify"><strong>3) Определяем </strong><strong>KPI</strong><strong>. </strong>Key Performance Indicators, что в переводе с английского - ключевые показатели эффективности. Для их определения мы изучаем особенности Вашей торговой площадки, особенности занимаемой Вами ниши, а также целевую аудиторию, которая будет попадать на Ваш сайт.</p>
<p align="justify"><strong>4) Запускаем рекламную кампанию. </strong>После подготовки рекламной компании наступает следующий этап – запуск. Для этого мы загружаем ее в систему управления (Google AdWords, Яндекс.Директ или другую). Процесс полностью автоматизирован, поэтому можно с легкостью импортировать даже огромное количество информации.</p>
<p align="justify"><strong>5) Анализируем и корректируем кампанию. </strong>Однако мало запустить кампанию, нужно за ней следить и постоянно корректировать для достижения максимального эффекта. Мы берем во внимание эффективность всех объявлений по ключевым словам и в зависимости от этого определяем – удалить или добавить дополнительные ключевики, подкорректировать текст объявления или поднять/понизить ставки. С помощью мощнейших инструментов аналитики мы можем проследить каждый шаг потенциального покупателя и подтолкнуть его на совершение покупки.</p>
