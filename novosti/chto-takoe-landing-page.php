                </a>
              </p>
              <p><span style="line-height:../1.5em;">Всем известно, что <strong>реклама</strong> – это основной способ для привлечения потенциальных <strong>клиентов</strong>. Она может принимать разнообразные формы, но цель у нее одна – внимание аудитории. В настоящее время огромной популярностью пользуются посадочные страницы или, как их еще называют, <strong>landing page</strong>.</span></p>
<p align="center"><img src="../resources/news/16/01/08/image001.png" alt="Что такое landing pages" width="600" height="227" /></p>
<p align="justify">Они позволяют добиться гораздо большей конверсии, нежели иные методы продвижения. В рамках данной статьи мы более подробно рассмотрим <strong>landing page</strong>, а также расскажем о том, какими принципами необходимо руководствоваться при его создании.</p>
<h2 align="center">Landing page: что такое и для чего необходимо</h2>
<p align="justify">Как Вы уже могли догадаться, данный <strong>термин</strong> имеет английское происхождение и перевести его можно как «<strong>целевая страница</strong>». Кроме того, ее называют продающей или посадочной страницей. <strong>Landing page</strong> – это сайт, содержащий одну страницу, единственной целью которой является продажа определенных товаров или услуг. Как правило, потенциальные клиенты попадают на одностраничник через поисковые системы, а также через баннеры, контекстные объявления, социальные сети и почтовые рассылки.</p>
<p align="justify">Создание <strong>landing page</strong> обусловлено несколькими причинами. Их можно разделить на 3 группы:</p>
<p>1. <strong>Продажа товара или услуги</strong> в рамках какой-либо акции или распродажи.</p>
<p>2. Побуждение пользователя подписаться на <a href="../uslugi/prodvizhenie-sajtov/e-mail-marketing.html">почтовую рассылку</a>, произвести <strong>регистрацию на сайте</strong> или совершить иное действие.</p>
<p>3. Побуждение пользователя к <strong>скачиванию или установке</strong> определенного программного обеспечения.</p>
<p align="justify">Если подытожить вышесказанное, то можно прийти к выводу, что основным предназначением лендинга является <strong>конверсия</strong> посетителя в <strong>покупателя</strong> или подписчика. К слову, одностраничные сайты с этим прекрасно справляются.</p>
<h2 align="center">Разновидности landing page: типы и виды</h2>
<p align="justify">Разобравшись с понятием <strong>лендинга</strong> можно переходить к следующему этапу – его <strong>типам</strong> и <strong>видам</strong>. Существует несколько вариантов реализации продающего сайта.</p>
<h3 align="center">Типы лендинга:</h3>
<ul>
<li>Располагается на основном сайте компании;</li>
<li>Небольшой ресурс из нескольких страниц;</li>
<li>Автономный сайт.</li>
</ul>
<p align="justify">1) Некоторые компании занимаются созданием <strong>лендинга</strong> прямо на <strong>основном сайт</strong>е. В некоторых случаях такой способ может быть оправдан, но, как правило, он не принесет положительного эффекта. Это обусловлено тем, что <a href="../uslugi/razrabotka-sajtov/korporativnyij-sajt/default.htm">корпоративные сайты</a> содержат лишние элементы в интерфейсе, которые только отвлекают пользователя от <strong>рекламной информации</strong>.</p>
<p align="justify">2) Целесообразнее использовать <strong>отдельный ресурс</strong>, который будет состоять из нескольких страниц исключительно <strong>рекламного характера</strong>. Они содержат подробную информацию о товаре не только в текстовом, но и в мультимедийном виде.</p>
<p align="justify">3) Однако наиболее распространенный вариант - <strong>создание</strong> продающего сайта с <strong>одной страницей</strong>. Он имеет минималистичный дизайн и содержит самую основную информацию, чтобы заинтересовать максимальное количество посетителей. Кроме того, он наполнен различными продающими слоганами и иными элементами, которые могут привлечь внимание (анимированные кнопки, таймеры обратного отсчета и т.д.). Одностраничный сайт является <strong>наиболее эффективным</strong> решением, позволяющим <strong>повысить конверсию</strong> в разы. На его разработку не требуется много времени и средств, а наличие конструкторов еще более упрощают задачу. Однако, если Вы создаете серьезный проект, то рекомендуем заказать продающий сайт у профессионалов.</p>
<p align="justify">Стоит отметить, что также существуют различные виды <strong>landing page</strong>.</p>
<h3 align="center">Виды лендинга:</h3>
<ul>
<li> Вирусная страница;</li>
<li> Лид-страница;</li>
<li> <a href="../uslugi/prodvizhenie-sajtov/kontekstnaya-reklama.html">Рекламная страница</a>.</li>
</ul>
<p align="justify">1) Особенность <strong>вирусной страницы</strong> в том, что она не содержит навязчивой рекламы. Она тщательно маскируется под видом развлекательного видеоролика, статьи, игры и т.д. В данном случае логотип компании располагается где-нибудь в углу и результат достигается посредством привыкания к развлечению, что, в свою очередь, прививает подсознательное доверие к компании.</p>
<p align="justify">2) <strong>Лид-страница</strong>, в большинстве случаев, применяется для маркетинговых исследований. С помощью них можно собрать более подробную информацию о целевой аудитории и сформировать грамотное предложение.</p>
<p align="justify">3) <a href="../uslugi/razrabotka-sajtov/lending.html">Разработка landing page</a> рекламного вида – это наиболее мощная и эффективная форма воздействия на сознание потенциальных покупателей. Она наполнена различной текстовой и мультимедийной информацией, которая всеми силами побуждает посетителя воспользоваться услугой или приобрести товар.</p>
<h2 align="center">Разработка landing page: основные моменты</h2>
<p align="justify">Прежде, чем начинать <strong>разработку</strong> одностраничника необходимо проанализировать все его составляющие. Их можно разделить на 3 вида:</p>
<p align="justify">1)<strong>Техническая</strong>. Страница состоит из различных элементов (HTML и CSS код, контент, мультимедийная информация и иные графические элементы).</p>
<p align="justify">2) <strong>Предпринимательская</strong>. Стоит помнить, что основной целью лендинга является совершение покупки.</p>
<p align="justify">3) <strong>Пользовательская</strong> - юзабилити сайта.</p>
<p align="justify">Приняв во внимание вышеперечисленные факторы, можно сформулировать правильные критерии, которые позволят создать <strong>эффективный лендинг.</strong> Главным из них можно считать призыв к действию. Для того, чтобы он начал работать, необходимо придерживаться следующих правил:</p>
<ul>
<li>Избегайте туманных формулировок, говорите <strong>четко и ясно</strong>;</li>
<li><strong>Не</strong> стоит слишком <strong>часто</strong> использовать различные призывы. Пользователь должен понять сам, что ему нужен данный товар;</li>
<li><strong>Используйте</strong> дополнительные визуальные <strong>элементы</strong>, которые способны привлечь внимание посетителя.</li>
</ul>
<p align="justify">Кроме того, при разработке <strong>landing page</strong> необходимо позаботиться о том, чтобы страница была простой и в тоже время привлекательной. Добиться этого можно следующими способами:</p>
<ul>
<li>Все составляющие страницы должны <strong>побуждать к</strong> определенному <strong>действию</strong>;</li>
<li><strong>Одна страница</strong> – <strong>один</strong> главный <strong>призыв</strong>;</li>
<li>Элементы не должны загромождать друг друга, на странице должно быть <strong>много пространства</strong>;</li>
