            <div class='content'>
              <p class='image'>
                <a href='sozdanie-personalnoj-reputaczii-pri-zapuske-internet-biznesa.html'>
                  <img alt='Создание персональной репутации при запуске интернет-бизнеса' src='../assets/cache_image/resources/web-business_1600x487_490.jpg'>
                </a>
              </p>
              <p align="justify">Если вы пробовали свои силы в интернет-бизнесе (интернет-магазины, сайты-статейники, консультирование и т.д.), то вы должны были заметить, что основным фактором от которого зависит успех вашего бизнеса является персональная репутация его основателя. Речь не о масштабных пиар кампаниях, как у кинозвезд или попсовых исполнителей. Все гораздо проще. Речь о том, что в глазах пользователей интернета вы должны выглядеть, как человек знающий свое дело. Это чрезвычайно важно. Сначала вы делитесь своим опытом с аудиторией, который ей так необходим, а потом эти люди станут фундаментом вашего бизнес проекта.</p>
<p align="justify">Очевидно, что начиная бизнес с чистого листа вам придется столкнуться с множеством проблем, в отличие от ситуации, когда ваша персональная репутация позволяет давать интервью в СМИ и вести экспертные колонки в сообществах.</p>
<p align="justify">Само собой, безупречная репутация не появляется за неделю. Как минимум нужно потратить 1 или 2 года. Однако, что делать если вы не можете ждать столько времени?</p>
<p align="center"><strong>Проанализируем пошаговый алгоритм действий на примере бизнесмена, который имеет в распоряжении несколько проектов.</strong></p>
<p align="justify">Данной информацией делится агентство «РепутаЦЦи», которая специализируется на создании положительного имиджа в сети.</p>
<p align="justify">Бизнесмен собирался получить кредит в зарубежном банке, чтобы развернуть там свой бизнес. У данного банка есть представительство внутри страны, а также за рубежом. После запроса о выдаче кредита ему пришел ответ, суть которого заключалась в том, что по клиенту отсутствует какая-либо информация в интернете, а это, естественно, не вяжется с образом успешного предпринимателя. Именно это событие натолкнуло его на мысль о том, что пора заняться своим имиджем в сети.</p>
<p align="center"><strong style="font-size: 18px;">Намеченные цели и чего удалось достичь спустя 4 месяца</strong></p>
<p align="justify"><strong>Что показал анализ: </strong>по главным запросам поисковики выдают актера с такой же фамилией.</p>
<p align="justify"><strong>Приоритетная цель: </strong>проработать образ успешного человека, который является экспертом в определенной области, имеет глубокие знания тематики своего бизнеса, появляется на публичных мероприятиях и имеет определенные успехи в своей деятельности, которые выражаются наградами, сертификатами и т.д.</p>
<p align="justify">Проводилась комплексная и разноплановая работа, которая позволила создать максимально естественный имидж.</p>
<h3 align="center"><strong>Используемые инструменты</strong></h3>
<p align="justify"><strong>1) Создание отзывов.</strong></p>
<p align="justify">Логично предположить, что если предприниматель имеет несколько проектов, то на них можно встретить какие-либо отзывы. Мы создали пару топиков о его бизнес-проектах на отзовиках. Там же мы опубликовали несколько отзывов (некоторые из них задним числом), в которых заказчик упоминался в позитивном ключе.</p>
<p align="justify"><strong>2) Наполнение блогов.</strong></p>
<p align="justify">Множество крупных новостных агентств позволяют вести своим пользователям различные блоги. Мы завели несколько блогов на таких площадках (исключительно деловой тематики) и зарегистрировали нашего клиента в качестве автора. После прохождения модерации мы на протяжении длительного времени выкладывали контент от имени заказчика. Основной целью данной деятельности служило создание доверия и преподнесение заказчика, как человека разбирающегося в своем деле. Кроме того, под каждыми постами публиковалось около 20 комментариев, которые придавали дополнительный эффект нашему продвижению.</p>
<p align="justify"><strong>3) Раскрутка аккаунтов в социальных сетях.</strong></p>
<p align="justify">Мы завели аккаунты на двух самых распространенных деловых площадках - LinkedIn и professionali.ru. Была проведена работа по наполнению профилей деловыми контактами (несколько десятков) и добавлению записей. Главной целью являлся вывод страниц в ТОП поисковой выдачи.</p>
<p align="justify"><strong>4) Публикация интервью.</strong></p>
<p align="justify">Мы опубликовали обширные интервью с нашим заказчиком на нескольких ресурсах деловой направленности.</p>
<h3 align="center"><strong>Итог проделанной работы:</strong></h3>
<p align="justify">Спустя несколько месяцев завершился основной этап работы. По ключевым имиджевым запросам поисковые системы выдают положительную информацию. Кроме того, весь ненужный контент может быть оперативно удален заказчиком.</p>
<p align="justify">Все это только основа, базовый уровень. Естественно, если все бросить и пустить на самотек, то результат исчезнет. Необходимо постоянно поддерживать свою репутацию и работать не покладая рук. В качестве примера, можно расширить список социальных сетей и добавить туда FaceBook. Она генерирует огромный объем трафика и хорошо котируется, как в странах СНГ, так и за рубежом. Не стоит бросать и деловые блоги, а хотя бы изредка публиковать новый контент с упоминанием вашего бизнеса и, конечно же, его основателя. Еще большую пользу принесет собственный <a href="../uslugi/razrabotka-sajtov/korporativnyij-sajt/default.htm">корпоративный сайт</a>, на котором можно публиковать статьи о вашем бизнесе, итоги работы, наставления начинающим бизнесменам и т.д.</p>
<p align="justify">Однако стоит помнить, что <a href="../uslugi/razrabotka-sajtov/default.htm">создание качественного сайта</a> – это кропотливый процесс, который нужно доверять только профессионалам своего дела. Обратившись в <a href="../default.htm">нашу веб-студию</a>, мы поможем создать конкурентоспособный проект любой сложности (начиная от простого <a href="../uslugi/razrabotka-sajtov/sajt-vizitka.html"> сайта-визитки</a> и заканчивая полнофункциональным <a href="../uslugi/razrabotka-sajtov/internet-magazin.html">интернет-магазином</a>).</p>
<p align="justify">Кроме того, вы даже можете написать собственную книгу! Все ограничивается только вашей фантазией. Вышеперечисленные инструменты помогут увеличить узнаваемость не только интернет-проектов, но и оффлайн бизнеса. Хотя, стоит отметить, что оффлайн бизнес в чистом виде постепенно исчезает. Любая серьезная компания осознает, что интернет – мощный инструмент, позволяющий значительно расширить целевую аудиторию.</p>
<p align="justify">Особого внимания заслуживает тот факт, что большая часть работы проводилась в ручном режиме. Естественно, использовались инструменты для автоматического постинга, отслеживания отзывов и так далее. Но главными инструментами являются 3 вещи:</p>
<ol>
