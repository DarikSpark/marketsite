</ul>
<ul>
<li>Благодаря <em>паттернам</em> человек экономит много времени и сил на то, что за него спокойно может сделать его мозг. Он избавляет нас от необходимости принимать решения там, где нужно всего лишь стереотипное поведение.</li>
</ul>
