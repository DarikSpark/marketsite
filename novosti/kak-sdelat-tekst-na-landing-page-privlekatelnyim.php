              <p align="center"> </p>
<p>Реакции на что-либо появляются благодаря работе нашего <strong>подсознания</strong>. К подсознательным реакциям можно отнести рефлексы, интуицию, инстинкты. И они работают быстрее, чем осознанные реакции. Поэтому, даже если нам кажется, что решения принимаем мы, опираясь на логику и рассуждения, на самом деле все наши действия происходят на уровне неосознанности. Как же можно использовать эти знания в <em>продающих текстах</em>?</p>
<h2 align="center">Шаблоны – хорошо ли это?</h2>
<p>В животном мире также существуют свои  модели поведения. Человек в этом смысле похож на животных, так как тоже имеет запрограммированные реакции на некий раздражитель. Только если у животных эти модели поведения достаточно жесткие, и реакции в большинстве случаев предсказуемые, то у человека еще на модель поведения накладываются личные качества.</p>
<p>Неосознанная реакция помогает человеку быстро, не задумываясь о деталях,  принимать правильное решение. Не требуется обдумывание мелочей и анализ всей информации. С годами у человека появляются определенные ответные реакции. Ощущения и восприятие на ту или иную ситуацию называются <em>триггеры</em>.</p>
<p>Поэтому можно сказать, что у шаблонного поведения и мышления есть свои преимущества. Благодаря этому человек экономит много времени и сил на то, что за него спокойно может сделать его мозг. Он избавляет нас от необходимости принимать решения там, где нужно всего лишь стереотипное поведение. Конечно, мозг – это в некоторой степени совершенная информационная машина, наш персональный компьютер. И как любую технику, его тоже не стоит перегружать.</p>
<p>Из всего сказанного можно увидеть, что без паттернов поведения нам в жизни не обойтись. Но есть в них и свои недостатки.</p>
<h2 align="center"><strong>Как работает влияние</strong></h2>
<p>Какие могут быть недостатки у шаблонного поведения? Например, человеком становится достаточно легко управлять. Для этого всего лишь нужно знать, какой подать ему сигнал, чтобы потом получить нужную ответную реакцию. При этом, сигнал часто оказывается ошибочным.</p>
<p>Это можно рассмотреть на следующем <strong>примере</strong>.</p>
<p>Мужчина на улице останавливает вас и просит дать ему телефон позвонить. Какова вероятность, что вы согласитесь? Конечно, на ваше решение повлияет множество факторов. Но самыми первыми и неконтролируемыми будут как раз реакции, которые вырабатывает наше подсознание. В данном случае, вы охватите весь образ человека: насколько он порядочно выглядит, как он одет, как говорит. Ваше тело само даст сигнал, стоит ли ему доверять и определит вашу реакцию.</p>
<p><img style="display: block; margin-left: auto; margin-right: auto;" title="Влияние на подсознание" src="../resources/news/16/7/boy.jpg" alt="" width="460" height="330" /></p>
<p>Большую роль будет играть пол. Если с такой же просьбой к вам подойдет женщина (при условии что вы – мужчина), реакция будет несколько отличаться. Скорей всего, вы согласитесь. Согласно исследованиям психологов, красивым людям отказывают реже. Еще реже отказывают женщинам. Это и есть наши паттерны.</p>
<p><img style="display: block; margin-left: auto; margin-right: auto;" title="Влияние на подсознание" src="../resources/news/16/7/girl.png" alt="" width="460" height="330" /></p>
<p>Бесспорно, женщина может оказаться в данном случае мошенницей. А мужчина, наоборот, порядочным. Вы даже можете потом об этом задуматься, но первичная реакция ответит за вас. В данной ситуации работает влияние. И его два спутника - <strong>социальное доказательство</strong> и<strong> расположение.</strong></p>
<p>Как же использовать всё это для <a href="../uslugi/razrabotka-sajtov/lending.html">посадочной страницы</a> и повышения её <a href="../uslugi/prodvizhenie-sajtov/uvelichenie-konversii-sajta.html">конверсии</a>?</p>
<h3 align="center"><strong>Располагаем клиента к себе</strong></h3>
<p><strong>В чем секрет</strong></p>
<p>Как мы уже писали выше, человек в первую очередь судит по тому, что он видит. Поэтому, внешняя привлекательность стоит на первом месте. Все знают популярную фразу, что «внешность обманчива», но вместе с тем охотно идут у нее на поводу. Это всё те же подсознательные ответные реакции.</p>
<p><strong>Применяем на практике</strong></p>
<p>В том случае, если на нашей landing page у нас присутствуют изображения с людьми, постарайтесь отнестись к этому серьезно. Взгляните на них со стороны клиента, который зайдет на вашу страницу и увидит их впервые. Какую реакцию они вызовут у него? Логично, что стоит избегать лиц, которые вызывают <strong>отрицательную</strong> реакцию. </p>
<p><img style="display: block; margin-left: auto; margin-right: auto;" title="Отталкивающее лицо" src="../resources/news/16/7/ugly.png" alt="Пример отталкивающего лица" width="500" height="414" /></p>
<p>И наоборот, использовать изображения с людьми, лица которых вызовут<strong> положительные</strong> эмоции. Например, люди с улыбкой на лице и хорошо одетые. «По одежке встречают», а улыбка обезоруживает.</p>
<p><img style="display: block; margin-left: auto; margin-right: auto;" title="Располагающие лица" src="../resources/news/16/7/stock.jpg" alt="" width="650" height="400" /></p>
<p><strong>Немного подсказок:</strong></p>
<ol>
<li>Для начала протестируйте свои изображения с помощью A/B тестирования, чтобы получить более высокий целевой показатель.</li>
<li>Будет лучше, если человек на изображении будет смотреть в сторону подписки.</li>
</ol>
<h3 align="center"><strong>Метод сравнения</strong></h3>
<p><strong>В чем секрет</strong></p>
<p>Человеку всегда проще сделать вывод на контрасте. Это могут быть вещи или события. Для примера возьмем ситуацию с решением задач по математике. Как понять: данная задача сложная или легкая? Мы попробуем сначала решить задачу в блоке А. Нам она покажется довольно простой. Затем мы перейдем к решению задачи в блоке В. И тут начинаем понимать, что задача в этом блоке намного труднее, чем в блоке А. А чем бы мы руководствовались, если бы сразу начали решать вторую задачу? Возможно, она не показалась бы нам такой уж трудной, если бы не было с чем ее сравнить.</p>
<p>Поэтому, можно с уверенностью сказать, что этот принцип сравнения работает всегда и при любых обстоятельствах. И его довольно трудно заметить.</p>
<p><strong>Применяем на практике</strong></p>
<p>Клиента может поставить в тупик ситуация, когда у него нет дополнительной информации. Ему не с чем сравнить и трудно сделать вывод. Например, цена товара покажется ему слишком высокой или слишком низкой.</p>
<p>Предоставьте ему <strong>возможность</strong> сравнить эти товары между собой. Будет лучше поместить рядом два товара с разной ценой. Благодаря этому, человеку будет легче ориентироваться, сравнить и сделать вывод о выгодности данного предложения.</p>
<p>Делаем вывод: если рядом будут стоять дорогой продукт и тот, который мы хотим продать, есть высокая вероятность того, что клиент приобретет тот товар, который нам нужно продать. Так как сравнив, он покажется ему более выгодным.</p>
<p><img style="display: block; margin-left: auto; margin-right: auto;" title="Метод сравнения" src="../resources/news/16/7/prize.jpg" alt="" width="930" height="313" /></p>
<h3 align="center"><strong>Влияние авторитета</strong></h3>
<p><strong>В чем секрет</strong></p>
<p>Кому человек поверит больше? Незнакомому человеку или известному? В какое заведение пойдет? В то, о котором нет отзывов или туда, где отзывов много, к тому же еще и положительных? Люди не любят ошибаться, поэтому чаще следуют советам других людей. Известные люди – это авторитеты в наших глазах. Мы пытаемся быть похожими на них и следовать их советам. Заметили, что для рекламы товара чаще всего приглашают известных людей? Появление в кадре знакомого лица вызовет положительную реакцию и высокую вероятность того, что данный товар будут покупать.</p>
<p><strong>Применяем на практике</strong></p>
<p>Для того чтобы это работало, просто поместите на страницу блок <strong>отзывы</strong>. Там человек сможет ознакомиться с мнениями других людей и сделать свои выводы. Также можно поместить информацию о сотрудничестве с известными людьми (если таковое имеется), чьи имена покажутся людям знакомыми или благодарности этих людей. Всё это произведет выгодное впечатление.</p>
<p>Согласитесь, что есть колоссальная разница в заголовках:</p>
<p>«Несколько секретов красоты»</p>
<p>и</p>
<p>«Несколько секретов красоты, которыми делится «Экс-Мисс Вселенная» Оксана Фёдорова.</p>
<p>Известное имя в данном случае вызовет положительную реакцию в сознании.</p>
<p> <img style="display: block; margin-left: auto; margin-right: auto;" title="Влияние авторитета" src="../resources/news/16/7/fedorova.png" alt="" width="580" height="359" /></p>
<h3 align="center"><strong>Реакция на дефицит</strong></h3>
<p><strong>В чем секрет</strong></p>
<p>Известно, что нехватка чего-либо во все времена вызывала у человека желание быстрее приобрести это, пока не стало слишком поздно. Какова наша реакция, когда мы ищем нужный товар и вдруг обнаруживаем, что только <strong>сегодня</strong> он продается со скидкой? Или когда мы хотим забронировать билет на самолет и видим надпись о том, что осталось <strong>всего лишь</strong> 3 свободных места? Всё это подстегивает нас думать быстрее и решаться. Это и есть та самая мотивация – необходимость обладать дефицитным товаром.</p>
<p><strong>Применяем на практике</strong></p>
<p>Поместите на <a href="../uslugi/razrabotka-sajtov/lending.html">landing page</a> скидку, ограниченное количество товаров, времени или другой вариант дефицита. Вы увидите, какую роль это окажет на выбор клиента.</p>
<p>Но не злоупотребляйте этим! Вся информация о недостаточном товаров, скоро истекающем времени – должна быть<strong> достоверной</strong>. Обман легко раскроется, вы потеряете потенциальных покупателей.</p>
<h3 align="center"><strong>Принцип большинства</strong></h3>
<p><strong>В чем секрет</strong></p>
<p>В главе «Влияние авторитета» мы косвенно уже касались этого принципа. Человек по природе своей существо коллективное. И если что-то делает большинство – то это считается правильным. Пусть каждый человек индивидуален, но все равно мнение группы будет учитываться больше, чем мнения одного человека. Другими словами, важен рейтинг мнений, который сэкономит нам время и энергию.</p>
<p><strong>Применяем на практике</strong></p>
<p>Можно поместить на странице рейтинг посещений, где клиент увидит, сколько людей до него посещали данную страницу и что заказывали. Большое количество отзывов также окажет хорошее влияние. Особенно, если будет возможность проверить, что отзывы оставлены реальными людьми. Для этого к отзывам можно добавить фотографии клиентов, которые писали эти отзывы.</p>
<p>Желательно, чтобы эти люди на фото с отзывами выглядели <strong>реально</strong> и своим видом соответствовали <strong>теме </strong>продукта. Смотря на людей на фотографиях, человек подсознательно будет их сравнить с самим собой. Только тогда он поверит и примет для себя их отзывы.</p>
<p><img style="display: block; margin-left: auto; margin-right: auto;" title="Пример реакции большинства" src="../resources/news/16/7/hotel.PNG" alt="" width="1029" height="556" /></p>
