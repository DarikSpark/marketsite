              <p align="justify">Под <strong>спамом</strong> подразумевается различная коммерческая информация, которая рассылается людям против их воли. Спам рассылается <strong>различными способами</strong>. Наиболее распространенные из них это:</p>
<div align="justify"><ol>
<li><strong> Электронный почтовый ящик. </strong>Наверняка многие из вас получали письма о волшебных кошельках и прочих способах быстрого «заработка». Все это можно отнести к почтовому спаму. Однако волшебные кошельки – это самое безобидное, что можно встретить. Очень часто подобные письма ведут на мошеннические сайты или на завирусованное программное обеспечение.</li>
<li><strong>СМС или интернет-мессенджеры. </strong>СМС-спам – это коммерческая информация, которая распространяется через мобильную связь. С появлением сервисов обмена быстрых сообщений появилась новая разновидность спама – SPIM (Spam Instant Messenger). В данном случае реклама приходит в Viber, Telegram messenger, What’s App и т.д.</li>
<li><strong> Социальные сети. </strong>Спамеры получают доступ к аккаунтам наивных пользователей и рассылают от их лица сообщения. Как правило, они содержат фишинговые ссылки или просьбы о переводе денежных средств на указанный счет и номер мобильного телефона.</li>
<li><strong>Блоги с немодерируемыми комментариями или редактируемые страницы. </strong>Здесь все предельно понятно. Если сайт имеет хоть какие-то показатели ТИЦ с открытыми и немодерируемыми комментариями, то он становится лакомым кусочком для спамеров. Кроме того, пользуются популярностью страницы со свободным редактированием (наподобие Википедии).</li>
<li><strong>Спам в поисковых системах. </strong>Данный вид спама позволяет манипулировать поисковой выдачей.</li>
</ol></div>
<p>В рамках данной статьи мы поговорим о наиболее интересном для нас виде – поисковом.</p>
<h2 align="center"><strong>Цель использования поискового спама</strong></h2>
<p align="justify">Как уже упоминалось выше, спамить – это значит рассылать рекламную информацию пользователям, которые в ней не заинтересованы. Все это происходит, чтобы привлечь его внимание и, соответственно, получить денежную выгоду.</p>
<p align="justify">Поисковый спам, в свою очередь, старается ввести в заблуждение не только людей, но и поисковых роботов. Ранжирование информации поисковыми системами строится на многих факторах. Поисковый спам оказывает на них чрезмерное влияние, поисковика начинает «тошнить» от переоптимизированного ресурса и он понижает его в выдаче.</p>
<h2 align="center"><strong>Разновидности поискового спама</strong></h2>
<p>Переоптимизация ресурса может выражаться в:</p>
<ul>
<li>Избытке ключевых слов (слишком много прямых вхождений, большая плотность и т.д.);</li>
<li>Избытке ссылочной массы (много неестественных и некачественных ссылок);</li>
<li>Избыток ключей в <a href="vliyanie-domennogo-imeni-na-seo-sajta.html">доменном имени</a>.</li>
</ul>
<p>Все это учитывается поисковиками и влияет на поисковую выдачу. К счастью, поисковики постоянно совершенствуются и обмануть их становится все сложнее.</p>
<h2 align="center"><strong>Переспам текста ключевыми словами</strong></h2>
<p>Данная разновидность спама выражается в частом употреблении ключевых слов в тексте. Например, как в данном тексте:</p>
<blockquote>Смотрите, мы пластиковые окна продаем, знаете, что мы продаем? Пластиковые окна. Пластиковые окна купить по низкой цене. Пластиковые окна, очень много пластиковых окон.</blockquote>
<p align="justify">Гугл в инструкции для оптимизаторов упоминал, что <strong>переизбыток ключевиков</strong> создает неудобство для посетителя. Из-за этого сайт может быть понижен в выдаче. Яндекс, в свою очередь, рекомендует использовать статистику поисковых запросов, однако лишь для того, чтобы узнать о потребностях интернет-пользователей. Иные меры воздействия на выдачу Яндекс активно пресекает.</p>
<p align="justify">Стоит отметить, что <strong>поисковый спам</strong> неприемлем и с точки зрения правил русского языка. Он именуется <strong>тавтологией</strong>, что является серьезной ошибкой.</p>
<h2 align="center"><strong>Избыток ключей в доменном имени</strong></h2>
<p align="justify">Оказывается и доменное имя возможно заспамить. Многие вебмастера вставляют ключевые слова прямо в домен. Здесь стоит посмотреть на ситуацию с точки зрения обычного пользователя. Домен <strong><em>купитьпластиковыеокнапонизкойцене.рф</em></strong> явно неудобен для прочтения, а это негативно отразится на его выдаче.</p>
<h2 align="center"><strong>Переспам тайтла и мета-данных</strong></h2>
<p>Здесь подразумевается избыточное использование ключевых слов в заголовках и описаниях статьи. Более подробно мы разберем этот момент в следующих статьях.</p>
<h2 align="center"><strong>Избыточная ссылочная масса</strong></h2>
<p>Под данным понятием подразумевается:</p>
<ul>
<li>
<div align="justify"><strong>Внутренняя переоптимизация. </strong>Например, вы проставляете 20 внутренних ссылок на идентичную статью с идентичным анкором «купить». Естественно, поисковик посчитает данную страницу переоптимизированной и применит к ней санкции.</div>
</li>
<li>
<div align="justify"><strong>Маскированные ссылки.</strong> Они не видны рядовым юзерам, зато видны поисковым роботам. Маскированные ссылки перенаправляют на веб-ресурсы, которые не имеют отношения к сайту и посетителям показываются иные страницы.</div>
</li>
</ul>
<h2 align="center"><strong>Что делают поисковики с заспамленными ресурсами </strong></h2>
<p align="justify">На самом деле заспамленность – это очень относительное понятие. Как уже упоминалось выше, оно подразумевает избыточную оптимизацию различных элементов сайта для высокого ранжирования. Заспамленность не возникает случайно, это осознанные действия, которые направленны на обман поисковой системы. Ресурс не несет никакой пользы для посетители и его позиции ослабевают. Снять фильтр поисковой системы не так-то просто, на это может уйти не один месяц.</p>
<p>Стоит помнить, что любая разновидность спама не является грамотным методом продвижения и не поощряется поисковыми системами. Сам Яндекс говорит об этом открыто. Доказательства предоставлены ниже:</p>
<p> </p>
<p align="center"><img title="Отказа в платной регистрации сайта в ЯК в связи с переоптимизацией" src="../resources/news/151224/image001.jpg" alt="Отказа в платной регистрации сайта в ЯК в связи с переоптимизацией" width="1104" height="399" /> </p>
<p align="center"><em>Официальный ответ администрации Яндекс.Каталога</em></p>
<p>Исходя из этого можно сделать некоторые выводы:</p>
<ul>
<li>Поисковикам известно о понятии «переоптимизация». Кроме того, они сами его активно используют.</li>
<li>Поисковики могут различать переоптимизированные сайты, что, в свою очередь, ведет к применению определенных санкций и понижению сайта в выдаче.</li>
<li>Поисковики могут менять свое отношение к сайту со временем и снимать наложенные санкции.</li>
</ul>
<p>Алгоритмы постоянно совершенствуется, а это говорит о том, что поисковые системы не такие уж прямолинейные и они могут адаптироваться к изменениям, происходящим на вашем сайте.</p>
<h2 align="center"><strong>Как предотвратить заспамленность?</strong></h2>
<p>Если вы руководствуетесь здравым смыслом, размещаете осмысленные внутренние ссылки, используете релевантные ключевые слова, тайтлы, наполняете сайт полезным и уникальным контентом, то у вас в любом случае не получится заспамить сайт.</p>
<p>Откровенно говоря, чтобы нарваться на санкции от поисковика, нужно делать это намеренно, в обход установленных рекомендаций для вебмастеров и для получения быстрого результата.</p>
