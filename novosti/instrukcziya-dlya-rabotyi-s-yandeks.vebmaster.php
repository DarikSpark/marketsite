            <div class='content'>
              <p class='image'>
                <a href='instrukcziya-dlya-rabotyi-s-yandeks.vebmaster.html'>
                  <img alt='Инструкция для работы с Яндекс.Вебмастер' src='../assets/cache_image/resources/ya-master_1600x487_490.jpg'>
                </a>
              </p>
              <p align="justify"><strong>Яндекс.Вебмастер </strong>– инструмент, который является своеобразным <strong>посредником</strong> между поисковой системой и вашим сайтом. Сервис был запущен в 2007 году и за 8 лет существования стал незаменимым помощником для многих веб-мастеров.</p>
<h3 align="center"><strong>Для чего необходим Яндекс.Вебмастер?</strong></h3>
<p align="justify">С помощью данного сервиса вы сможете дать сигнал поисковой системе, о том, что появился <strong>новый сайт</strong>, на который ей необходимо обратить свое внимание.</p>
<p align="justify">Как всегда, все начинается с регистрации почтового ящика на Яндексе (если у вас его еще нет). Он будет служить вашим логином для Вебмастера.</p>
<p align="justify">После этого задача становится сложнее. Необходимо предоставить доказательства, что сайт действительно ваш. Для этого нужно <strong>разместить мета-тег</strong>, предложенный сервисом, на главной странице сайта. Однако подробнее на этом моменте мы остановимся позднее, а сейчас продолжим знакомство с остальными функциями.</p>
<h3 align="center"><strong>Для чего необходимо добавлять сайт в Яндекс.Вебмастер?</strong></h3>
<p align="justify">Первым по счету идет раздел «Общая информация». С помощью него вы сможете узнать о количестве внешних ссылок и страниц в поиске, а также узнать когда в последний раз заходил поисковый робот и сколько ему удалось загрузить страниц. Ну и, естественно, просмотреть данные о тематическом индексе цитирования (ТИЦ).</p>
<p align="center"><img title="Информация о сайте в Я. Вебмастер" src="../resources/news/151215/image001.jpg" alt="Информация о сайте в Я. Вебмастер" width="647" height="188" /></p>
<p align="center"><em>Так выглядит общая информация о сайте на Яндекс.Вебмастер.</em></p>
<h2 align="center"><strong>Раздел «Проверить </strong><strong>URL</strong><strong>»</strong></h2>
<p align="justify">Здесь вы сможете проверить наличие определенной страницы веб-ресурса в поисковой системе. Для этого просто введите ее адрес и кликните на кнопку «Проверить URL».</p>
<p align="center"><a href="../../convertmonster.ru/wp-content/uploads/_25D0_25AF_25D0_25BD_25D0_25B4_25D0_25B5_25D0_25BA_25D1_2581__25D0_2592_25D0_25B5_25D0_25B1_25D0_25B02D16D870E" target="_blank"><img title="Проверка индексации страницы в Яндекс.Вебмастер" src="../resources/news/151215/image002.jpg" alt="Проверка индексации страницы в Яндекс.Вебмастер" width="623" height="122" /></a></p>
<p align="center"><em>Так происходит проверка индексации страницы в Яндекс.Вебмастер.</em></p>
<p align="justify">Несмотря на кажущуюся простоту, данный раздел имеет неочевидные функции, о которых мы <a href="instrukciya-dlya-raboti-v-yandeks.wordstat.html">поговорим в следующих статьях</a>.</p>
<h3 align="center"><strong>Подраздел «Индексирование сайта»</strong></h3>
<p align="justify">Очень важно, чтобы все страницы ресурса были <strong>правильно</strong> проиндексированы и <strong>корректно</strong> отображались в поисковой выдаче.</p>
<p align="justify">Для того, чтобы поисковый робот загрузил страницы сайта нужно, чтобы он на них как-то попал. Чаще всего это происходит после перехода с другой внутренней страницы ресурса. Поэтому стоит уделить огромное внимание <strong>грамотной перелинковке</strong> статей, а также создать <strong>карту сайта</strong> (как для роботов так и для пользователей). Посмотреть, как Яндекс видит навигацию вашего сайта можно в разделе «Структура сайта».</p>
<p align="center"><a href="../../convertmonster.ru/wp-content/uploads/_25D0_25AF_25D0_25BD_25D0_25B4_25D0_25B5_25D0_25BA_25D1_2581__25D0_2592_25D0_25B5_25D0_25B1_25D0_25B00F3108752" target="_blank"><img title="«Структура сайта» в сервисе Яндекс.Вебмастер" src="../resources/news/151215/image003.jpg" alt="«Структура сайта» в сервисе Яндекс.Вебмастер" width="226" height="333" /></a></p>
<p align="center"><em>Пункт «Структура сайта» в сервисе Яндекс.Вебмастер.</em></p>
<p align="justify">Кроме того, стоит обратить внимание на конкретные страницы отображающиеся в поисковой выдаче.</p>
<h3 align="center"><strong>Подраздел «История»</strong></h3>
<p align="justify">Сервис способен отражать историю обращений робота к страницам сайта. Открыв вкладку «Количество запросов» вы увидите график доступности сайта.</p>
<p align="justify">На вкладке «HTTP коды» вы получите более подробную информацию о том, какой ответ возвращал сервер поисковому роботу. Наиболее распространенные из них:</p>
<p align="justify"><strong>1) Зеленый график (ОК)</strong> – все работает корректно.</p>
<p align="justify"><strong>2) Синий график (3ХХ)</strong> – означает, что страница перенаправляет на другой URL.</p>
<p align="justify"><strong>3) Красный график (4ХХ)</strong> – ошибка доступа клиентского запроса.</p>
<p align="justify">В интерфейсе Яндекс.Вебмастера это выглядит следующим образом:</p>
<p align="center"><img title="График ответа сервера в Яндекс.Вебмастер" src="../resources/news/151215/image004.jpg" alt="График ответа сервера в Яндекс.Вебмастер" width="640" height="345" /></p>
<p align="center"><em>График ответа сервера в Яндекс.Вебмастер.</em></p>
<h2 align="center"><strong>Раздел «Настройка индексирования»</strong></h2>
<p align="justify">Чтобы исключить из индекса мусорные страницы (например, страницы постраничной навигации, теги и др.) нужно установить запрет на их индексацию в специальном файле – robots.txt.</p>
<p align="justify">Правильно составленный robots.txt – еще один немаловажный шаг, который позволит поисковому роботу корректно проиндексировать ваш сайт. Описание тонкостей работы с robots.txt займет не одну страницу, поэтому более подробно о нем мы расскажем в следующих статьях.</p>
<h3 align="center"><strong>Подраздел «Главное зеркало»</strong></h3>
<p align="justify">Для того, чтобы не потерять ваш ресурс после склеивания доменов, необходимо определиться с главным зеркалом сайта.</p>
<ul>
<li>Для начала нужно выбрать <strong>главный домен</strong> (стоит помнить, что он может начинаться с префикса WWW). Если этого не сделать, то сервис определит основной домен автоматически. При таком раскладе все ссылки, установленные не на основное зеркало потеряют вес. </li>
<li>Если у вас есть 2 сайта с одинаковым контентом, которые расположены на разных доменах, то риск применения санкций от Яндекса очень велик. Указание главного зеркала поможет <strong>избежать бана</strong> в поисковой системе.  </li>
</ul>
<h2 align="center"><strong>Раздел «Поисковые запросы»</strong></h2>
<p align="justify">Яндекс.Вебмастер предоставляет возможность просматривать запросы, по которым сайт чаще всего показывался в <a href="../uslugi/prodvizhenie-sajtov/default.htm">поисковой </a>выдаче Яндекса. Сделать это можно в разделе «Поисковые запросы». Открыв раздел, вы увидите таблицу с запросами, разбитую на 4 колонки. Ее можно отсортировать по показам или кликам. Большим плюсом является возможность сохранения таблицы в файл Excel.</p>
<h2 align="center"><strong>Раздел «География сайта»</strong></h2>
<p align="justify">Ваш сайт имеет региональную направленность? Тогда данная функция окажется весьма полезной. Раздел «География сайта» может помочь подняться в выдаче <strong>определенного региона</strong>, так как Яндекс использует особенные методы ранжирования для ресурсов имеющих географическую привязку.</p>
<h3 align="center"><strong>Функция «Оригинальные тексты»</strong></h3>
<p align="justify">Яндекс.Вебмастер имеет очень полезную функцию, которая позволит защитить контент, размещаемый на страницах вашего сайта.</p>
