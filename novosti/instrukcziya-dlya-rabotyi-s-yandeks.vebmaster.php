<p align="center"><em>Функция «Оригинальные тексты»</em></p>
<p align="justify">Загрузив свой текст в специальную форму, вы дадите сигнал Яндексу об <strong>истинном авторстве</strong> контента. Это обезопасит вас от случаев, когда кто-то нагло ворует ваши труды, до появления их в индексе поисковой системы.</p>
<h2 align="center"><strong>Раздел «Сайт в результатах поиска»</strong></h2>
<p align="justify">В этом разделе находится пункт «Быстрые ссылки». Они представляют из себя набор популярных ссылок, которые наиболее интересны посетителям.</p>
<p align="center"><a href="../../convertmonster.ru/wp-content/uploads/convert_monster.jpg" target="_blank"><img title="Быстрые ссылки в поисковой выдаче" src="../resources/news/151215/image006.jpg" alt="Быстрые ссылки в поисковой выдаче" width="558" height="192" /></a></p>
<p align="center"><em>Быстрые ссылки в поисковой выдаче.</em></p>
<p align="justify">К сожалению, система определяет их <strong>автоматически</strong>. Указать вручную их невозможно. Однако есть некоторые рекомендации, позволяющие помочь поисковому роботу корректно определить быстрые ссылки для вашего сайта. Опять-таки, их выполнение не дает никаких гарантий, что Яндекс будет их учитывать.</p>
<p align="justify">Подраздел «Регистр имени сайта» может пригодится в том случае, если вы захотите подкорректировать название сайта изменив прописные буквы на строчные (или наоборот). Это никак не отразится на позициях в поисковой системе.</p>
