              <p class='image'>
                <a href='prizyiv-k-dejstviyu-na-landing-page-primeryi.html'>
                  <img alt='Призыв к действию на landing page - примеры' src='../assets/cache_image/resources/call-to-action_1600x487_490.jpg'>
                </a>
              </p>
              <p><span style="line-height:../1.5em;">Важнейшим составляющим высокой конверсии сайта является Call to Action (CTA), что в переводе с английского означает призыв к действию. В данной статье мы постараемся разобраться каким должен быть идеальный CTA в различных сферах деятельности:</span></p>
<ul>
<li>Розничная торговля;</li>
<li>Оптовая торговля;</li>
<li><a href="../uslugi/default.htm">Предоставление услуг</a>.</li>
</ul>
<p>Но для начала расскажем о 3-ех главных принципах, которые применимы к любому одностраничному сайту независимо от выбранной тематики.</p>
<h2><strong>3 главных принципа</strong></h2>
<p><strong>1) Легкость.</strong></p>
<p>Под легкостью подразумевается то, что пользователю предлагается выполнить действие, которое не требует ничего взамен. Например, получить бесплатную рассылку или несколько бесплатных вариантов дизайн-проекта. Приобретение товара нельзя отнести к данному пункту, так как действие подразумевает расставание с круглой суммой денег, а к таким действиям посетитель готов далеко не всегда. Это его отпугнет и он покинет Ваш одностраничник.</p>
<p><strong>2) Естественность.</strong></p>
<p>Если целевая аудитория Вашей компании – это люди, которые хотят заказать чай пуэр, чтобы выпить его в кругу семьи, то не стоит выставлять оптовый прайс лист или другие ненужные предложения. Это только запутает посетителя и он снова покинет Вашу страницу. Более разумным, в данном случае, было бы предложить пробники или консультацию.</p>
<p><strong>3) Ценность.</strong></p>
<p>Посетители неохотно отдают свои контактные данные, поэтому нужно предложить им что-нибудь в замен. Например – единовременную консультацию со специалистом. В таком случае пользователь с большим удовольствием заполнит все необходимые поля, так как он может получить важную информацию или вовсе решить свою проблему.</p>
<p>Разобравшись с основными принципами можно переходить к примерам CTA предложений на сайтах различной направленности.</p>
<h2><strong>Призыв к действию в розничной тематике</strong></h2>
<p> <img title="тест-драйв фотобокса" src="../resources/news/16/01/30/1.jpg" alt="CTA на примере сайта, который предлагает записаться на бесплатный тест-драйв фотобокса" width="1338" height="628" /></p>
<p><strong style="line-height: 1.5em;">CTA на примере сайта, который предлагает записаться на бесплатный тест-драйв фотобокса.</strong></p>
<p>Отличный пример, который отвечает всем вышеперечисленным принципам. Во-первых, данное предложение легкое и не требует ничего взамен. Если не понравится, то Вы можете просто уйти. Во-вторых, его ценность заключается в том, что пользователь может бесплатно опробовать товар в действии. Кроме того, используется дефицит во времени, который побуждает не откладывать дело в долгий ящик и записаться на тест-драйв прямо сейчас.</p>
<p> <img title=" СТА шкафов-купе" src="../resources/news/16/01/30/2.jpg" alt="CTA предложение при продаже различной мебели (в данном случае шкафов-купе)." width="1334" height="575" /></p>
<p><strong>CTA предложение при продаже различной мебели (в данном случае шкафов-купе).</strong></p>
<p>Здесь акцент делается на том, что посетителю нужны конкретные цены на изготовление шкафа-купе. Однако их не может быть в принципе, так как он выполняется индивидуально. Этим отлично пользуется данная компания и предлагает произвести расчет нескольких вариантов. Это подталкивает человека на действие и он останавливается на варианте, который больше всего отвечает его финансовым требованиям.</p>
<p> <img title="Заполните контактную форму и получите бесплатный каталог." src="../resources/news/16/01/30/3.jpg" alt="Заполните контактную форму и получите бесплатный каталог." width="1299" height="590" /></p>
<p><strong>Заполните контактную форму и получите бесплатный <a href="../uslugi/razrabotka-sajtov/korporativnyij-sajt/default.htm">каталог</a>.</strong></p>
<p>Перед нами очередной одностраничный сайт. Однако здесь возникают определенные сложности: «как разместить более двухсот наименований товаров на одной странице?». Создатели данного сайта нашли грамотное решение данной проблемы. Размещенная часть продукции на главной странице завлекает пользователя, а возможность получения бесплатного каталога подталкивает его к действию.</p>
<h2><strong>Призыв к действию в оптовой тематике</strong></h2>
<p> <img title="CTA предложение о получении оптовых цен." src="../resources/news/16/01/30/5.jpg" alt="CTA предложение о получении оптовых цен." width="1341" height="667" /></p>
<p><strong>CTA предложение о получении оптовых цен.</strong></p>
<p>Естественно, оптовым закупщикам не нужно предлагать никаких бесплатных каталогов, рассылок, так как это не принесет необходимого результата. В первую очередь их интересуют цены и возможность выжать максимум прибыли благодаря проданному товару. Очевидно, что единственно верным вариантом является предложение о получении прайс-листа.</p>
<h2><strong>Призыв к действию в сфере услуг</strong></h2>
<p>Данная ниша очень широка, поэтому сложно посоветовать что-либо конкретное. CTA предложение полностью зависит от выбранной Вами тематики. В данном случае необходимо придумать такое предложение, которое будет отвечать 3-ем основным принципам, перечисленным в начале данной статьи. Конечно же мы приведем несколько примеров, но не стоит их бездумно перенимать.</p>
<p> <img src="../resources/news/16/01/30/4.jpg" alt="Предложение, которое позволяет опробовать услугу с помощью первого бесплатного занятия" width="1344" height="639" /></p>
<p><strong>Предложение, которое позволяет опробовать услугу с помощью первого бесплатного занятия</strong><em>.</em></p>
<p>Для разработки landing page, которая предлагает любое обучение (начиная от вязания крестиком и заканчивая курсами английского языка, как в данном случае), самым оптимальным решением станет получение первого урока бесплатно. Здесь, в большей степени, задействованы принципы легкости и ценности. Увидев данное предложение человек поймет, что он ничего не потеряет, если посетит первый урок, который предоставляется бесплатно.</p>
<p> <img title="Достаточно самоуверенный призыв к действию, который может вызвать неоднозначную реакцию." src="../resources/news/16/01/30/6.jpg" alt="Достаточно самоуверенный призыв к действию, который может вызвать неоднозначную реакцию." width="1339" height="667" /></p>
<p><strong>Достаточно самоуверенный призыв к действию, который может вызвать неоднозначную реакцию.</strong></p>
<p>Пример демонстрирует, что нет стандартных CTA решений, которые принесут высокую конверсию. Данная компания занимается реализацией специальных скриптов, которые позволяют поднять эффективность продаж. Их CTA является своеобразным вызовом: «Не веришь? Тогда мы продемонстрируем на своем примере, как нужно продавать». Однако этот призыв к действию выглядит немного дерзко, поэтому рекомендуем использовать его с осторожностью.</p>
